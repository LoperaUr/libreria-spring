package com.example.libreria.domain;

public class UbicacionD {
    private int ubicacionId;
    private String ubicacionPiso;
    private String ubicacionSalon;
    private String ubicacionEstante;

    public int getUbicacionId() {
        return ubicacionId;
    }

    public void setUbicacionId(int ubicacionId) {
        this.ubicacionId = ubicacionId;
    }

    public String getUbicacionPiso() {
        return ubicacionPiso;
    }

    public void setUbicacionPiso(String ubicacionPiso) {
        this.ubicacionPiso = ubicacionPiso;
    }

    public String getUbicacionSalon() {
        return ubicacionSalon;
    }

    public void setUbicacionSalon(String ubicacionSalon) {
        this.ubicacionSalon = ubicacionSalon;
    }

    public String getUbicacionEstante() {
        return ubicacionEstante;
    }

    public void setUbicacionEstante(String ubicacionEstante) {
        this.ubicacionEstante = ubicacionEstante;
    }
}
